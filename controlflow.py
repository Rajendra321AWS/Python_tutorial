"""Writing program using if/else"""

def main():
    """Run basic if/else program for age and temperature"""

# integer variable
    age = 25

# float variables
    temperature = 35.8

# using if/else with integers
    if age >= 25:
        print("your are an adult")
    else:
        print("your are an minor")

# using if/else with float variables
    if temperature >= 30.5:
        print("Temperature is height")
    else:
        print("temperature is normal")
if __name__ == "__main__":
    main()
