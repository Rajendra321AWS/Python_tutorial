"""Basic Math python program with addition multiplication"""

def main():
    """Basic arithmetic program"""

    # integers
    a = 10
    b = 5

    addition = a + b
    subtraction = a - b
    multiplication = a * b
    division = a / b

    print("Number A:", a)
    print("Number B:", b)
    print("Addition:", addition)
    print("Subtraction:", subtraction)
    print("Multiplication:", multiplication)
    print("Division:", division)

if __name__ =="__main__":
    main()
